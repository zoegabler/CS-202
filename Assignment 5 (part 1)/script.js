    function renderMedia(item) {
      switch (item.mediaType) {
        case 'Image':
          return `<img src="${item.mediaURL}" alt="${item.title}" />`;
        case 'Video':
          return `<iframe src="${item.mediaURL}" frameborder="0" allowfullscreen></iframe>`;
        case 'Audio':
          return `<audio controls src="${item.mediaURL}"></audio>`;
        default:
          return '';
      }
    }